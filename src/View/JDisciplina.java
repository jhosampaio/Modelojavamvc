    private void tblDiscMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDiscMouseClicked
        int index = tblDisc.getSelectedRow();
        if(index >= 0 && index<controle.lista.size()){
            Disciplina disc = controle.lista.get(index);
            textDisc_Turma.setText(disc.getTurma());
            textDisc_Nome.setText(disc.getNome());
        }
    }//GEN-LAST:event_tblDiscMouseClicked

