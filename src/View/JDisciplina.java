    public void LoadTable (){
        DefaultTableModel modelo = new DefaultTableModel (new Object[]{"Turma","Nome"},0);
        
        for (int i=0 ; i<controle.lista.size();i++){
            Object linha[] = new Object []{controle.lista.get(i).getTurma(),controle.lista.get(i).getNome()};
            modelo.addRow(linha);
        
        }
        tblDisc.setModel(modelo);
        tblDisc.getColumnModel().getColumn(0).setPreferredWidth(50);
        tblDisc.getColumnModel().getColumn(1).setPreferredWidth(200);        
        tblDisc.setModel(modelo);
    
    }

