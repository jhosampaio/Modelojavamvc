    public void ManipulaInterface(){
        switch(modoInterface){
            case "Navegar":
                btnDisc_Salvar.setEnabled(false);
                btnDisc_Cancelar.setEnabled(false);
                textDisc_Turma.setEnabled(false);
                textDisc_Nome.setEnabled(false);
                btnDisc_Novo.setEnabled(true);
                btnDisc_Editar.setEnabled(false);
                btnDisc_Excluir.setEnabled(false);
                btnDisc_Gravar.setEnabled(false);
                btnDisc_Ler.setEnabled(true);
                break;
            case "Novo":
                btnDisc_Salvar.setEnabled(true);
                btnDisc_Cancelar.setEnabled(true);
                textDisc_Turma.setEnabled(true);
                textDisc_Nome.setEnabled(true);
                btnDisc_Novo.setEnabled(false);
                btnDisc_Editar.setEnabled(false);
                btnDisc_Excluir.setEnabled(false);
                btnDisc_Gravar.setEnabled(false);
                btnDisc_Ler.setEnabled(true);
                
                break;
                
            case "Editar":
                btnDisc_Salvar.setEnabled(true);
                btnDisc_Cancelar.setEnabled(true);
                textDisc_Turma.setEnabled(true);
                textDisc_Nome.setEnabled(true);
                btnDisc_Novo.setEnabled(true);
                btnDisc_Editar.setEnabled(false);
                btnDisc_Excluir.setEnabled(false);
                btnDisc_Gravar.setEnabled(true);
                btnDisc_Ler.setEnabled(true);
                break;
                
            case "Excluir":
                btnDisc_Salvar.setEnabled(false);
                btnDisc_Cancelar.setEnabled(false);
                textDisc_Turma.setEnabled(false);
                textDisc_Nome.setEnabled(false);
                btnDisc_Novo.setEnabled(true);
                btnDisc_Editar.setEnabled(false);
                btnDisc_Excluir.setEnabled(false);
                btnDisc_Gravar.setEnabled(true);
                btnDisc_Ler.setEnabled(true);
                break;
                
            case "Selecao":
                btnDisc_Salvar.setEnabled(false);
                btnDisc_Cancelar.setEnabled(false);
                textDisc_Turma.setEnabled(false);
                textDisc_Nome.setEnabled(false);
                btnDisc_Novo.setEnabled(true);
                btnDisc_Editar.setEnabled(true);
                btnDisc_Excluir.setEnabled(true);
                btnDisc_Gravar.setEnabled(true);
                btnDisc_Ler.setEnabled(true);
                break;
            default: System.out.println("Modo invÃ¡lido");
        }
    }

